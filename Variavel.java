class Variavel{
	public int intValor;
	public double doubleValor;
	public float floatValor;
	public String nome;

	public setInt(int parametro){
		this.intValor = parametro;
	} 

	public setDouble(double parametro){
		this.doubleValor = parametro;
	}

	public setFloat(float parametro){
		this.floatValor = parametro;
	}

	public setString(String parametro){
		this.nome = parametro;
	} 
}